ALTER TABLE dbo.DriversStandings ADD CONSTRAINT PK_DriversStandings_DriverStandingID PRIMARY KEY (DriverStandingID);

CREATE TABLE dbo.DriversSeasons
(
	ID INT IDENTITY(1,1) NOT NULL,
	DriverID INT NOT NULL,	
	SeasonRefID INT
);

ALTER TABLE dbo.DriversSeasons ADD CONSTRAINT PK_DriversSeasons_ID PRIMARY KEY (ID);

CREATE TABLE dbo.DriversChampionships
(
	ID INT IDENTITY(1,1) NOT NULL,
	DriverID  INT NOT NULL,	
	SeasonRefID INT
)

CREATE TABLE dbo.Constructors
(
	ID INT IDENTITY(1,1) NOT NULL,
	ConstructorID INT NOT NULL,
	Constructor varchar(100),		
	CountryID INT,	
	RacesEntered INT,	
	RacesStarted INT, 	
	Drivers INT,	
	TotalEntries INT,	
	Wins INT,	
	Points INT,	
	Poles INT,	
	FL INT,	
	Podiums INT,	
	WCC INT,
	WDC INT,
);

ALTER TABLE dbo.Constructors ADD CONSTRAINT PK_Constructors_ConstructorID PRIMARY KEY (ConstructorID);

CREATE TABLE dbo.ConstructorsSeasons
(
	ID INT IDENTITY(1,1) NOT NULL,
	ConstructorID INT,
	SeasonID INT
);

ALTER TABLE dbo.ConstructorsSeasons ADD CONSTRAINT PK_ConstructorsSeasons_ID PRIMARY KEY (ID);

CREATE TABLE dbo.ConstructorStandings
(
ConstructorStandingID INT NOT NULL IDENTITY(1,1),
ConstructorID INT,
SeasonID INT,
Points DECIMAL(6,2) DEFAULT 0,
DateAdded DATETIME DEFAULT GETDATE(),
LastUpdated DATETIME 
);

ALTER TABLE dbo.ConstructorStandings ADD CONSTRAINT PK_ConstructorStandings_ID PRIMARY KEY (ConstructorStandingID);

CREATE TABLE dbo.ConstructorNationality
(
	ID INT IDENTITY(1,1) NOT NULL,
	ContructorID INT,	
	CountryID INT
);

ALTER TABLE dbo.ConstructorNationality ADD CONSTRAINT PK_ConstructorNationality_ID PRIMARY KEY (ID);

CREATE TABLE dbo.Circuits
(
	CircuitID INT NOT NULL,
	Circuit varchar(200) NOT NULL,	
	GrandsPrix varchar(200) NOT NULL,		
	TypeRefID INT,	
	DirectionRefID INT,	
	LastLengthUsed DECIMAL(5,3),	
	GrandsPrixHeld INT
