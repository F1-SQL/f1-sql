ALTER TABLE dbo.Circuits ADD CONSTRAINT PK_Circuits_CircuitID PRIMARY KEY (CircuitID);

CREATE TABLE dbo.CircuitImages
(
	ImageID INT IDENTITY(1,1) NOT NULL,
	CircuitID INT,
	ImageURL nvarchar(1000)
